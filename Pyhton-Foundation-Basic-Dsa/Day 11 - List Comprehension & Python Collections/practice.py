# List Comprehension
numbers = [1, 2, 3, 4, 5]
squares = [n*n for n in numbers]
print(squares)
# Filtering with condition
even_numbers = [n for n in numbers if n % 2 == 0]
print(even_numbers)
# Tuple Basics
point = (10, 20)
print(point[0], point[1])
# Set Basics
names = {"Isam", "Rana", "Isam"}
print(names)
